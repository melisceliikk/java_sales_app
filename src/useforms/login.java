    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String username = txtUsername.getText().trim();
        String password = String.valueOf(txtPassword.getPassword()).trim();
        
        if ( username.equals("") || password.equals("") ) {
            JOptionPane.showMessageDialog(rootPane, "Kullanıcı adı yada şifre boş olamaz!");
        }else {
            UserModel md = new UserModel();
            int statu = md.userLogin(username, password);
            if (statu == -1) {
                JOptionPane.showMessageDialog(rootPane, "Kullanıcı adı yada şifre hatalı!");
            }else {
                // user statu ?
                if (statu == 0) {
                    // Persons
                    Persons ps = new Persons();
                    ps.setVisible(true);
                    dispose();
                }
                
                if (statu == 1) {
                    // Admin
                    Admin ad = new Admin();
                    ad.setVisible(true);
                    dispose();
                }
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

